using System.Diagnostics.CodeAnalysis;
using System.Web;
using System.Web.Http;

namespace Simon.UI.Web.Areas.HelpPage
{
    /// <summary>
    /// Use this class to customize the Help Page.
    /// For example you can set a custom <see cref="System.Web.Http.Description.IDocumentationProvider"/> to supply the documentation
    /// or you can provide the samples for the requests/responses.
    /// </summary>
    public static class HelpPageConfig
    {
        [SuppressMessage("Microsoft.Globalization", "CA1303:Do not pass literals as localized parameters",
            MessageId = "Simon.UI.Web.Areas.HelpPage.TextSample.#ctor(System.String)",
            Justification = "End users may choose to merge this string with existing localized resources.")]
        [SuppressMessage("Microsoft.Naming", "CA2204:Literals should be spelled correctly",
            MessageId = "bsonspec",
            Justification = "Part of a URI.")]
        public static void Register(HttpConfiguration config)
        {
            var documentPath = HttpContext.Current.Server.MapPath("~/ApiData/Simon.Api.Web.xml");

            config.SetDocumentationProvider(new XmlDocumentationProvider(documentPath));
        }
    }
}